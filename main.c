void softreset() {
  disable_irqs();
  // stop clocks
  RCC->AHB1ENR = 0;
  RCC->AHB2ENR = 0;
  RCC->AHB3ENR = 0;
  RCC->APB1ENR = 0;
  RCC->APB2ENR = 0;

  // clear RAM
  memset((void*) 0x20000000,0, 0x20000);

  SCB->AIRCR  = (NVIC_AIRCR_VECTKEY | (SCB->AIRCR & (0x700)) | (1<<NVIC_SYSRESETREQ)); /* Keep priority group unchanged */
  asm("dsb"); /* Ensure completion of memory access */
  while(1);
}

