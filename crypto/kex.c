static const char* pgpword(uint16_t *wordlist, uint8_t n, size_t idx) {
  return((char*) wordlist+wordlist[(idx%2)*256 + n]);
}

static int find_peer(uint8_t* msg) {
  size_t i;
  for(i=0;i<peers;i++) {
    if(memcmp((void*) msgs+i*33+5, (void*) msg+5, 32-5)==0) return i;
  }
  return -1;
}

static void accept_secret(char ignored) {
  unsigned int ptr;
  if(peer_len<1)
    getstr("pls name key", peer, &peer_len);
  ptr = store_seed(key, peer, peer_len);
  memset(key, 0, 32);
  if(ptr<1) {
    oled_clear();
    statusline();
    oled_print(0,16, "failed to store key", Font_8x8);
    return;
  }
  mode=KEXTYPE;
  show_verifier=0;
  gui_refresh=1;
}

static int show_pgpwords() {
  int ret = menu(&appctx, (const uint8_t**) menuitems,menuitems_len,accept_secret);
  if(ret == 0) {
    memset(key,0, 32);
    mode=KEXTYPE;
    show_verifier=0;
    gui_refresh=1;
  }
  return ret;
}

static void to_pgpwords(const unsigned char* buf, const size_t buflen) {
  int i;
  pgpwords = bufs[0].buf;
  LZG_Decode(pgpwords_lzg, PGPWORDS_LZG_LEN, pgpwords, PGPWORDS_SIZE);
  for(i=0;i<buflen;i++) {
    menuitems[i]=(uint8_t*) pgpword((uint16_t *)pgpwords, buf[i], i);
  }
  menuitems_len = buflen;
}

